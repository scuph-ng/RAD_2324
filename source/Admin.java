class Admin{
  int id;
  String name;
  String contact;
  String email;
  String passwd;
  int storeID;

  public Admin(int id, String name, String contact, String email, String passwd, int storeID) {
    this.id = id;
    this.name = name;
    this.contact = contact;
    this.email = email;
    this.passwd = passwd;
    this.storeID = storeID;
  }
}
