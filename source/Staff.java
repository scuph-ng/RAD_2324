class Staff{
  int id;
  String name;
  String contact;
  String email;
  String passwd;
  int adminID;

  public Admin(int id, String name, String contact, String email, String passwd, int adminID) {
    this.id = id;
    this.name = name;
    this.contact = contact;
    this.email = email;
    this.passwd = passwd;
    this.adminID = adminID;
  }
}
